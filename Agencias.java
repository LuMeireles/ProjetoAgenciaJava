package br.com.crud.model;

public class Agencias {

	private int Cadastur;
	private String Nome;
	private String Telefone;
	private String Endereco;
	private String GuiaTuristico;
	
	public int getCadastur() {
		return Cadastur;
	}
	public void setCadastur(int cadastur) {
		Cadastur = cadastur;
	}
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getTelefone() {
		return Telefone;
	}
	public void setTelefone(String telefone) {
		Telefone = telefone;
	}
	public String getEndereco() {
		return Endereco;
	}
	public void setEndereco(String endereco) {
		Endereco = endereco;
	}
	public String getGuiaTuristico() {
		return GuiaTuristico;
	}
	public void setGuiaTuristico(String guiaTuristico) {
		GuiaTuristico = guiaTuristico;
	}
	
}
